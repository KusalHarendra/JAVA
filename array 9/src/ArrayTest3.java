public class ArrayTest3 {
    public static void main(String[] args) {

        String[] names = new String[5];     //create an array with 5 spaces of elements

        names[0] = "kusal";     //adding elements
        names[2] = "kasun";
        names[3] = "amal";
        names[4] = "kamal";

        System.out.println(names[0]);
        System.out.println(names[1]);       //no element
        System.out.println(names[2]);
        System.out.println(names[3]);
        System.out.println(names[4]);
    }
}
